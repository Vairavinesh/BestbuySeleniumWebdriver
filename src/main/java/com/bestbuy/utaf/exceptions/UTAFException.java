package com.bestbuy.utaf.exceptions;

/**
 * @author Kalaiselvi Jaganathan
 *
 */
public class UTAFException extends Exception {
	private static final long serialVersionUID = 1L;
	
	/**
	 * Constructor
	 * 
	 */
	public UTAFException(){
		super();
	}
	
	/**
	 * Constructor with args
	 * 
	 * @param causedBy
	 */
	public UTAFException(Throwable causedBy) {
		super(causedBy);
	}
	
	/**
	 * Constructor with args
	 * 
	 * @param message
	 */
	public UTAFException(String message) {
		super(message);
	}
	
	/**
	 * Controller with args
	 * 
	 * @param message
	 * @param causedBy
	 */
	public UTAFException(String message,Throwable causedBy) {
		super(message, causedBy);
	}
}